""" 
Una variable es un contenedor de información
 """

# crear variables
texto = "Curso ERP/CRM"
texto2 = "Odoo"
numero = 25
decimal = 7.8

# imprimir variables
print(texto)
print(texto2)
print(numero)
print(decimal)

# concatenar / concatenación
nombre = "Rafael"
apellido = "Martín"
curso = "ERP/CRM"

print(nombre + " " + apellido + " " + curso)
print(f"Hola soy {nombre} {apellido} estoy en el curso {curso}")
print("Hola soy {} {} estoy en el curso {}".format(nombre,apellido,curso))
